C
C
C	*************** ANALOGUE EXPERIMENTAL ROUTINE ****************
C
C	PURPOSE IS TO MODEL AN ENVIRONMENTAL CONTROL OF A THERMODYNAMIC
C	SYSTEM. IN PARTICULAR WE ARE CONSIDERING A SET OF 32 MOTEL UNITS
C	(SOLWAY MOTOR INN) HAVING:
C	1. HEATED FLOOR SLAB OF CAPACITY ...  1680 WH/DEG.C
C	2. SLAB-TO-ROOM CONDUCTANCE OF   ...  100 W/DEG.C
C	3. ROOM-TO-AMBIENT CONDUCTANCE OF ..  88  W/DEG.C
C	4. ZERO ROOM CAPACITY
C	5. SLAB HEATING OF CAPACITY      ...  2 kW
C	6. SLAB ON 2300-0700 WITH 'STAT AT .  25 DEG.C WHILE TAMB<14 DEG.C
C	7. PANEL HEATER ON 1700-2300, STAT .  20 DEG.C
C
C
C	-----SPECIAL SETUP PRIOR TO JANUARY INITIAL CONDITIONS-----------
500	MONTH=1			!FIRST ITERATION THIS YEAR, START JANUARY.
	DO 502,NHOUR=1,24
	Q1(NHOUR)=0.
	Q2(NHOUR)=0.
502	TROOM(NHOUR,2)=17.1	!PRETEND "DECEMBER RESULT MASTERTON=17.1'C
	ITER=2			!FLAT AND CONVERGED ON SECOND ITERATION AND
	TSLAB=17.1		!SLAB LEFT AT 17.1'C MASTERTONMEAN AT 0700.
C
C	   -------------INITIAL CONDITIONS FOR ANY MONTH---------
505	DO 506,NHOUR=1,24
506	TROOM(NHOUR,1)=TROOM(NHOUR,ITER)  !START WITH LAST MONTHS RESULT
	NHOUR=7		!BEGIN NEW MONTH AT EIGHTH HOUR STARTING 0700 ON INCR
C
C	TROOM1=TROOM(7,1)!CARRY ON FROM LAST MONTHS 0700 TROOM##############
C	   ABOVE LINE NOT USED AS ROOM HAS NO STORAGE, ONLY SLAB DOES.######
C
	PERHR=4		!START THIS MONTHDAY WITH 4 STEPS/HR
	STEP=1./FLOAT(PERHR)	!STEP SIZE IN HOURS
	ELOST=999.		!DUMMY LAST-E FOR FIRST CONV. TEST
	ELAST=599.		!DUMMY LAST-ELAST FOR FIRST CONV TEST.
C	NOTE THAT ELOST,ELAST,E ARE TOTAL DIFFERENCES IN TEMP OVER SUCCESSIVE
C	DAY ITERATIONS, USED TO CHECK CONVERGENCE OF RESULT ARRAY TROOM(HR,I)
	TAMB=DAYTMP(NHOUR,MONTH,TMAX,TMIN)
	TROOM(NHOUR,ITER)=(100.*TSLAB+88.*TAMB)/188. !USE LAST MONTHS TSLAB
	TYPE 9501,(INT(DAYTMP(N,MONTH,TMAX,TMIN)),N=1,24)!$$$$$$$$$DEBUG
9501	FORMAT (X'AMBS',24I3)				!$$$$$$$$$$DEBUG
C
C	-------------------------HOUR STEP ITERATION----------------------
510	NHOUR=NHOUR+1
	ITERA=0
	Q1(NHOUR)=0.
	Q2(NHOUR)=0.
	TAMB=DAYTMP(NHOUR,MONTH,TMAX,TMIN)
	IF (NHOUR.GE.18.AND.NHOUR.LT.23)GO TO 512	!ROOM MUST >=20
	IF ((NHOUR.EQ.24.OR.NHOUR.LT.8).AND.TAMB.GE. 14.0)GO TO 514!
C	   ---- DEFAULT OF NO HEATING ON -----
511	TSLAB1=TSLAB
	ITERA=ITERA+1
	TROOM(NHOUR,ITER)=TROOM1
	IF (ITERA.EQ.(PERHR+1))GO TO 519
	TSLAB=TSLAB-STEP*(TSLAB-TAMB)*46.8/1680.
C		46.8W/DEG.C SLAB-AMBIENT ,1680 W.H/DEG.C SLAB SPHT
	TROOM1=(100.*TSLAB+88.*TAMB)/188.
	GO TO 511
C
C	   ---- 1700 - 2300 PANEL HEATER GETS T>=20 DEG.C -----
512	TSLAB1=TSLAB
	ITERA=ITERA+1
	TROOM(NHOUR,ITER)=TROOM1	!SET FOR EXIT TO NEXT HOUR
	IF (ITERA.EQ.(PERHR+1))GO TO 519	!FROM HERE.
	TROOM2=(100.*TSLAB1+88.*TAMB)/188.!TO DECIDE IF HEAT NEEDED
	IF(TROOM2.GE. 20.)GO TO 513	!ELSE CONTINUE FIND HEAT INPUT
	DELTAQ=(20.-TROOM2)*188.*STEP	!W.H PER STEP TO KEEP 20 DEG.C
	Q1(NHOUR)=Q1(NHOUR)+DELTAQ	!WHERE 188 IS NORTON GEN CONDUCT.
	TSLAB=TSLAB-STEP*(TSLAB-20.)*100./1680.	!DEG.C PER HR/100
	TROOM1=TROOM2+DELTAQ/(188.*STEP)!THIS CALC WILL ALWAYS GET 20 DEG
C					!WOULD NEED ONLY IF PANEL O/P LIMITED
	GO TO 512	!SLAB EXCH. W. ROOM, PANEL HTR. KEEPS 20 DEG.
513	TSLAB=TSLAB-STEP*(TSLAB-TAMB)*46.8/1680.!NOHEAT,SLAB<->AMBT EXCH
	TROOM1=TROOM2	!ROOM FLOATING ABOVE 20 THANKS TO SLAB, AMBIENT
	GO TO 512	
C
C	   ---- 2300 - 0700 SLAB GETS UP TO 25 DEG.C IF TAMB <14 DEG.C ---
514	TSLAB1=TSLAB
	ITERA=ITERA+1
	TROOM(NHOUR,ITER)=TROOM1
	IF (ITERA.EQ.(PERHR+1))GO TO 519
	TROOM1=(100.*TSLAB1+88.*TAMB)/188.!THIS IS STARTING POINT FOR HOUR
	IF(TSLAB1.GE. 25.)GO TO 518	!ELSE CONTINUE FIND HEAT INPUT
	DELTAQ=STEP*(25.-TSLAB1)*1680.	!W.H PER STEP TO GET 25 DEG.C SLAB
	DELTAQ=DELTAQ+STEP*(TSLAB1-TAMB)*46.8!NOT FORGETTING SLAB LOSS
	IF(DELTAQ.LE.(2000.*STEP))GO TO 515 !(IF U/FLOOR SATISFIES DEMAND)
	DELTAQ=2000.*STEP		!ELSE U/FLOOR SATURATES AT 2kW
515	TSLAB=TSLAB+DELTAQ/1680.	!ACTUAL TSLAB INCREASE BY SLAB HTG
	TSLAB=TSLAB-STEP*(TSLAB-TAMB)*46.8/1680.!AND DECREASE (LOSS TO AMB)
	Q2(NHOUR)=Q2(NHOUR)+DELTAQ	!UPDATE SLAB KWH INPUT FOR STEP
	TROOM1=(100.*TSLAB+88.*TAMB)/188.!UPDATE ROOM TEMP
	GO TO 514
518	TSLAB=TSLAB-STEP*(TSLAB-TAMB)*46.8/1680.!NATURAL LOSS TO AMB ONLY
	TROOM1=(100.*TSLAB+88.*TAMB)/188.!UPDATE ROOM TEMP
	GO TO 514	
C	-------------------HOUR STEP TIMING LOGIC--------------------
519	IF (NHOUR.LT.24)GO TO 520
	NHOUR=0		!TO GO DO HOUR ONE ON INCREMENTING.
520	IF (NHOUR.NE.7)GO TO 510	!ELSE THIS ITER 0700-0700 FINISHED
C	------------------CONVERGENCE TEST FOR MONTH----------------------
	TYPE 9510,MONTH,(INT(10.*TROOM(N,ITER)),N=1,24)
9510	FORMAT (X'M',I2,'T',24I3)
	E=0
	DO 530 N=1,24
530	E=E+ABS(TROOM(N,ITER)-TROOM(N,ITER-1))
	IF (E.LT. 1.0)GO TO 560			!CONVERGED TO 1/24 DEG DIF
	IF (E.LT.ELAST)GO TO 532	!NOT DIVERGING
	PERHR=PERHR*2		!HALVE STEP SIZE AND MAYBE DO MONTH AGAIN
	STEP=1./FLOAT(PERHR)	!
	IF (ELAST.GT.ELOST)GO TO 550	!DIVERGING TWICE IN ROW
532	TYPE 9530,PERHR,E/24.,ELAST/24.,ELOST/24.
9530	FORMAT (X'STEPS/HR= ',I3,'AVDEGERRORS(NOW,-1,-2)',3F8.4)
	ITER=ITER+1
	ELOST=ELAST	!UPDATE HISTORICAL DIFFERENCES RIPPLING
	ELAST=E		!THROUGH E,ELAST,ELOST
	NHOUR=7		!REITERATE BEGIN EIGHTH HOUR STARTING 0700 ON INCR
	GO TO 510	!NHOUR TO 8 TO CARRY ON WHERE WE LEFT OFF.
550	TYPE 9550,ELOST,ELAST,E
9550	FORMAT (X'DIVERGING FOR 2 SUCCESSIVE ITERATIONS AVE TDIFFS
	1'/X'STARTING OLDEST BEING ',3F8.4)
	GO TO 360
C	--------------------TAKE CONVERGED RESULT-----------------------
560	COSTQ1(MONTH)=0.
	COSTQ2(MONTH)=0.
	DO 562,NHOUR=1,24			!SUM COSTS OVER MONTH
	COSTQ1(MONTH)=COSTQ1(MONTH)+Q1(NHOUR)*30.5*32./1000.
562	COSTQ2(MONTH)=COSTQ2(MONTH)+Q1(NHOUR)*30.5*32./1000.
580	MONTH=MONTH+1
	IF (MONTH.EQ.13)GO TO 590
	DO 582,N=1,24
582	TROOM(N,1)=TROOM(N,ITER)	!RESET ITER CNTR AND DO NEXT MONTH
	ITER=2
	GO TO 505
590	Q1COST=0.
	Q2COST=0.
	DO 592,MONTH=1,12			!SUM COSTS OVER YEAR
	Q1COST=Q1COST+COSTQ1(MONTH)
592	Q2COST=Q2COST+COSTQ2(MONTH)
	Q1COST=Q1COST*.117	!PANEL 1700-2300 WAIARARAPA DAY TARIFF
	Q2COST=Q2COST*.038	!SLAB  2300-0700 WAIARARAPA NITE TARIFF
	TYPE 9590,Q2COST,Q1COST
9590	FORMAT (X'SLAB ON NITE $',F8.2,'  PANEL ON DAY TRFF
	1 $',F8.2,' PA')
599	GO TO 360
C
C	******************** WRITE DATA TO DISK *************************
C
900	TYPE 9900
9900	FORMAT(X'DO YOU WANT TO SAVE THE DATA (1) OR LEAVE 
	1DISK FILE (DEGDAY.TMP) UNALTERED (2)?')
	ACCEPT 9902, NANS
9902	FORMAT(I2)
	IF (NANS .EQ. 2) GOTO 999
	OPEN (UNIT = 11, NAME = 'DEGDAY.TMP', TYPE='NEW')
	WRITE (11, 9904) NSITE
9904	FORMAT(20A1)
	WRITE (11, 9906) TMAX
9906	FORMAT(12F6.2)
	WRITE (11, 9906) TMIN
	WRITE (11, 9907) NMASKM
9907	FORMAT (I2)
	DO 914, J=1,15
914	WRITE (11, 9908)(NMMASK(J,K),K=1,12)
	WRITE (11, 9907) NMASKH
	DO 915, J=1,15
915	WRITE (11, 9910) (NHMASK(J,K),K=1,24)
9908	FORMAT(12(I2,' '))
9910	FORMAT(24(I2,' '))
	CLOSE (UNIT=11)
999	CONTINUE
	STOP
	END
C
C
C	FUNCTION TO FORMULATE TEMPERATURE AT A SITE HAVING MAX, MIN MONTHLY
C	AVERAGE TEMPS ASSUMING MIN AT 0530 AND MAX AT 1330 AND SINUSOID
C	BEHAVIOUR BETWEEN THESE TIMES.
C
	FUNCTION DAYTMP (NTIME,MONTH,TMAX,TMIN)
	DIMENSION TMAX(12),TMIN(12)
	PI = 3.141593	!NOTE THAT TIME (INTEGER HOUR) IS FOR AVE OVER THAT
	DAYTMP= 1E10	!HOUR, HOURS DEFINED AS FROM 1 TO 24 NOT INCL 0
	TAVE=(TMAX(MONTH)+TMIN(MONTH))/2
	TAMPLT=(TMAX(MONTH)-TMIN(MONTH))/2
	TIME=FLOAT(NTIME)
	IF(NTIME .GE. 6 .AND. NTIME .LT. 14) GOTO 10
	IF(NTIME .GE. 14 .AND. NTIME .LE. 24) GOTO 20
	IF(NTIME .GE. 1 .AND. NTIME .LT. 6) GOTO 30
	GO TO 50
10	DAYTMP=TAVE-TAMPLT*COS((TIME-6.)*PI/8.)		!6AM TO 2PM
	GO TO 50
20	DAYTMP=TAVE+TAMPLT*COS((TIME-14.)*PI/16.)	!2PM TO MDNT
	GO TO 50
30	DAYTMP=TAVE+TAMPLT*COS((TIME+10.)*PI/16.)	!MDNT TO 6AM
50	RETURN
	END
