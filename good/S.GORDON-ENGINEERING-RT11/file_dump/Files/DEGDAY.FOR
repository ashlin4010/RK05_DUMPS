9000	FORMAT(X'DO YOU WANT TO USE PREVIOUS RUN DATA (1) 
	1(DEGDAY.TMP) OR MANUAL ENTRY (2)? ')
	ACCEPT 9001 ,NDATIN
9001	FORMAT (I1)
	IF (NDATIN.EQ.2)GO TO 40
