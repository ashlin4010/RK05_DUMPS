C	TEMPERATURE AND ENERGY TARIFF CONSIDERATIONS.
C
	DIMENSION TMAX(12),TMIN(12),NMMASK(15,12),NHMASK(15,24)
	DIMENSION NM(20),NH(20),TSPACE(20),COND(20),ECOST(20)
	DIMENSION COSTCL(20),COSTHT(20),TROOM(24,15),Q1(24),Q2(24)
	DIMENSION COSTQ1(12),COSTQ2(12)
	BYTE NSITE(20)
	INTEGER PERHR
C
	NSYST=0
10	NOWHAT=0!FLAGS NEW SECTION ENTRY FOLLOWED BY GOTO 360 MULTIPLE CHOICE
