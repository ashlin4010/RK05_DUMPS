	SUBROUTINE SHOMSK(N1,N2,N3,N4)
	DIMENSION  N3(15,12), N4(15,24)
	TYPE 1
1	FORMAT (X'MONTH MASKS FROM JANUARY TO DECEMBER:'
	1/X'NO.  JAN FEB MAR APR MAY JUN JUL AUG SEP OCT NOV DEC')
	DO 5,NMM=1,N1
	TYPE 2,NMM,(N3(NMM,NM),NM=1,12)
2	FORMAT (X'#',I2,':',12I4)
5	CONTINUE
	TYPE 10
10	FORMAT (X'DIURNAL MASKS FROM HOUR 1 TO HOUR 24 (MDNT TO
	1 MDNT)'/X'NO.  01 02 03 04 05 06 07 08 09 10 11 12 13 14 15
	2 16 17 18 19 20 21 22 23 24')
	DO 15,NHM=1,N2
	TYPE 12,NHM,(N4(NHM,NH),NH=1,24)
12	FORMAT (X'#',I2,':',24I3)
15	CONTINUE
	RETURN
	END

