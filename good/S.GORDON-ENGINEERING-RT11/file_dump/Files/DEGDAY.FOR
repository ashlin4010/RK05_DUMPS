	TYPE 9101,MASKMT
9101	FORMAT (X'FOR MONTH MASK #',I2,'ENTER MASK WEIGHT DIGIT PAIR OR '/
	1' -VE TO ABORT.')
145	MONTH=MONTH+1
	TYPE 9102,MONTH
9102	FORMAT (X'DURING MONTH',I2,' WEIGHT =',$)
	ACCEPT 9103,MT
9103	FORMAT (I2)
	IF (MT.EQ.-1)GO TO 129	!REENTER THIS MASK!
	IF (MT.EQ.-2)GO TO 160	!FINISHED MONTH MASKS!
	NMMASK(MASKMT,MONTH)=MT
	IF(MONTH.EQ.12)GO TO 130!GO START NEXT MASK!
	GO TO 145		!ELSE DO NEXT MONTH!
160	NMASKM=MASKMT-1	!TOTAL NUMBER OF MONTH MASKSETS DEFINED.
	IF (NOWHAT.NE.0)GO TO 360	!IF WE REENTERED GO CHOOSE NEXT TASK
C
C	******************* NOW ENTER DIURNAL MASKSETS ****************
C
200	TYPE 9200,NMASKM
	IF (NOWHAT.EQ.0)GO TO 220	!(IF THIS IS FIRST ENTRY THIS ROUTINE)
	MASKHR=NMASKH+1	!ELSE CONTINUE ADDING HOUR
	GO TO 240	!MASKS WHERE WE LEFT OFF
9200	FORMAT (XI2,'MONTH MASKSETS HAVE BEEN GENERATED. NOW ENTER DIURNAL
	1(HOUR BASED)'/' MASKS ON SAME BASIS AS FOR MONTHS WITH TWO-DIGIT CODE
	2FOR WEIGHT '/' OR -1 (REENTRY) / -2 (EXIT) CONTROLS. FIRST MASK
	3IS AGAIN PREDEFINED'/' THIS TIME AS <ALL DAY LONG> SO START BY
	4ENTERING SECOND MASK IF REQUIRED.')
220	MASKHR=1
	DO 225,NHOUR=1,24
225	NHMASK(MASKHR,NHOUR)=10
	MASKHR=2
229	MASKHR=MASKHR-1	!REENTER HERE FOR MASK REPEAT!
230	MASKHR=MASKHR+1	!REENTER HERE FOR NEXT MASK!
240	NHOUR=0
	TYPE 9201,MASKHR
9201	FORMAT (X'FOR DIURNAL MASKSET #',I2,'ENTER MASK WEIGHT DIGIT
	1PAIR OR '/' -VE CODE TO ABORT. NOTE THAT HOUR 1 BEGINS AT 
	2MIDNIGHT')
245	NHOUR=NHOUR+1
	TYPE 9202,NHOUR
9202	FORMAT (X'DURING HOUR ',I2,'WEIGHT =',$)
	ACCEPT 9203,MH
9203	FORMAT (I2)
	IF (MH.EQ.-1)GO TO 229	!REENTER THIS MASK!
	IF (MH.EQ.-2)GO TO 260	!FINISHED DIURNAL MASKS!
	NHMASK(MASKHR,NHOUR)=MH
	IF(NHOUR.EQ.24)GO TO 230!GO START NEXT MASK!
	GO TO 245		!ELSE DO NEXT HOUR!
260	NMASKH=MASKHR-1	!TOTAL NUMBER OF DIURNAL MASKS DEFINED.
	IF (NOWHAT.NE.0)GO TO 360	!IF THIS WAS REENTERED GO CHOOSE TASK
C
C	************** ENTRY OF TEMPERATURE/MASK MIXES *******************
C
300	NSYS=1
	IF(NOWHAT.EQ.0)GO TO 306	!IF FIRST TIME THROUGH
	NSYS=NSYST+1	!ELSE CONTINUE WHERE WE
	CALL SHOMSK(NMASKM,NMASKH,NMMASK,NHMASK)
	PAUSE 'TYPE <CR> TO CONTINUE WHEN MASKS HAVE BEEN REVIEWED'
305	GO TO 310	!LEFT OFF LAST TIME
306	TYPE 9301,NMASKH,NMASKM
9301	FORMAT (XI2,' DIURNAL MASKS HAVE BEEN GENERATED.  THESE ARE SET OUT
	1 TOGETHER WITH ',I2,' MONTH'/' MASKS BELOW TO ASSIST YOU WITH NEXT
	2 PHASE:')
	CALL SHOMSK(NMASKM,NMASKH,NMMASK,NHMASK)
310	TYPE 9302
9302	FORMAT (X'ENTER MASK COMBINATIONS, DESIGN TEMPERATURE, NETT
	1 CONDUCTANCES [kW/deg C] TO'/X'AMBIENT TEMPERATURE HEATSINK
	2 (USE 1.0 WHERE DEGREE-HOURS'/' REQUIRED RATHER THAN KWH), AS
	3 FOLLOWS:'/' TERMINATE WITH COMMAS UNTIL LINE-END'/' THEN
	4 <CR>. USE -VE MONTHMASK,REMAINDER ZEROES TO EXIT,'/X'-VE
	5 $PERkWH TO REPEAT LINE (TO COVER ERROR)')
319	NSYS=NSYS-1
320	NSYS=NSYS+1
	TYPE 9303,NSYS
9303	FORMAT (X' SYSTEM,MONTHMASK,DIURNALMASK,TEMP.,CONDUCTANCE,$PERkWH'
	1/X'SYST ',I2,' : ',$)
	ACCEPT 9304,NM(NSYS),NH(NSYS),TSPACE(NSYS),COND(NSYS),ECOST(NSYS)
9304	FORMAT (I5,I5,F8.4,F8.4,F8.4)
	IF (ECOST(NSYS).LT.0) GO TO 319
	IF (NM(NSYS).LT.0) GO TO 355
C	  ** NOW CALCULATE HEATING AND COOLING FOR THIS (SUB)SYSTEM **
C	MONTH=0
	COSTCL(NSYS)=0.
	COSTHT(NSYS)=0.
	DO 340, MONTH=1,12
	TYPE 9306,MONTH				!$$$$$$$$$$$$DEBUG
9306	FORMAT (X'CALCULATING MONTH ',I2) 	!$$$$$$$$$$$$DEBUG
	LM=NMMASK(NM(NSYS),MONTH)
	IF (LM.EQ.0)GO TO 340
	DO 338, NHOUR=1,24
	LH=NHMASK(NH(NSYS),NHOUR)
	IF (LH.EQ.0)GO TO 338
	I=MONTH	!DUMMIES NECESS TO SUPRESS WARNING FOR "POSSIBLE"
	J=NHOUR	!DO VARIABLE MODIFICATION IN DAYTMP CALL:
	TAMB=DAYTMP(J,I,TMAX,TMIN)
	COSTMP=FLOAT(LM*LH)/100.*(TSPACE(NSYS)-TAMB)
	1*COND(NSYS)*ECOST(NSYS)
	COSTMP=COSTMP*30.5	!30.5 DAYS IN AVE. MONTH.
	IF (COSTMP)332,338,333
332	COSTCL(NSYS)=COSTCL(NSYS)-COSTMP	!NB MINUS FOR +VE COOLING
	GO TO 338
333	COSTHT(NSYS)=COSTHT(NSYS)+COSTMP	!NB PLUS FOR +VE HTG LOAD
338	CONTINUE  !BOTH CONTINUES NECESS TO SUPRESS DO ENTRY WARNINGS
340	CONTINUE
	TYPE 9305,COSTHT(NSYS),COSTCL(NSYS)	!SYSTEM RESULT TO VDU
9305	FORMAT (X'$/DEG.HOURS(AS DEFINED):   - HEATING = ',F8.1,
	1',   - COOLING = ',F8.1)
C
	GO TO 320	!GO DO NEXT SYSTEM
C
355	NSYST=NSYS-1	!TOTAL NO. OF SYSTEMS DEFINED.
360	TYPE 9310
9310	FORMAT (X'NOTE THAT SYSTEMS ARE NOT SAVED ON EXIT'
	1/X'DO YOU WANT TO:-'
	2/X'(1) CONCLUDE WITH OPTIONAL WEATHER, MASKS UPDATE TO DISK'
	3/X'(2) GO BACK TO NEW MIX'
	4/X'(3) DEFINE NEW DIURNAL MASK'
	5/X'(4) DEFINE NEW ANNUAL MASK'
	6/X'(5) DEFINE NEW WEATHER DATA'
	7/X'(6) RUN SIMULATION ANALOGUE MODEL')
	ACCEPT 9311,NOWHAT
9311	FORMAT (I1)
	GO TO (900,300,200,100,10,500),NOWHAT
