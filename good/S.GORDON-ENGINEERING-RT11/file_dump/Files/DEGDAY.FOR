	OPEN (UNIT = 11, NAME = 'DEGDAY.TMP', TYPE='OLD')
	READ (11, 9904) NSITE
	READ (11, 9906) TMAX
	READ (11, 9906) TMIN
	READ (11, 9907) NMASKM
	DO 14, J=1,15
14	READ (11, 9908)(NMMASK(J,K),K=1,12)
	READ (11, 9907) NMASKH
	DO 15, J=1,15
15	READ (11, 9910) (NHMASK(J,K),K=1,24)
	CLOSE (UNIT=11)
	GOTO 360
C
C	******************* ENTER NEW WEATHER STATION DATA ***************
C
40	TYPE 9002
9002	FORMAT (X'ENTER NAME OF WEATHER STATION AS UP TO 20 
	1CHARACTERS, <CR>')
	ACCEPT 9003,NCHRS,(NSITE(I),I=1,NCHRS)
9003	FORMAT(Q,20A1)
	TYPE 9004
9004	FORMAT (X'ENTER LATITUDE, LONGITUDE, ALTITUDE OF WEATHER STATION 
	1AS LL,LL,LL,LL,AAAA<CR>'/' IN DEG, MIN (SOUTH=+VE),DEG,MIN(E OF 
	2GREENWICH), METRES.')
	ACCEPT 9005
9005	FORMAT (4I5, I6)
	TYPE 9006
9006	FORMAT (X'ENTER MONTHLY AVE TMIN AND TMAX DEG. CELSIUS FOR EACH 
	1MONTH AS FOLLOWS '/' MONTH  TMIN    TMAX')
	DO 50, MONTH=1, 12
	TYPE 9007, MONTH
9007	FORMAT (X'   ',I2,'   ',$)
	ACCEPT 9008, TMIN (MONTH),TMAX(MONTH)
9008	FORMAT (2F5.1)
50	CONTINUE
C
C	********************* NOW ENTER MONTH MASKSETS *******************
C
100	TYPE 9100
	IF (NOWHAT.EQ.0)GO TO 120	!(IF FIRST ENTRY THIS ROUTINE)
	MASKMT=NMASKM	!ELSE CONTINUE ADDING MONTH MASKS!
	GO TO 130	!WHERE WE LEFT OFF!	
9100	FORMAT (X'ENTER MONTH COMBINATION MASK SETS AS INSTRUCTED,STARTING
	1WITH THE SECOND;'/' THE FIRST IS A DEFAULT OF <ALL YEAR ROUND>.
	2  FOR EACH MONTH ENTER A 2 DIGIT'/' NUMBER BETWEEN 00 AND 10 AS A 
	3WEIGHTING OUT OF TEN FOR THE MONTH. -1 REENTERS THE'/' CURRENT MASK
	4 IF AN ERROR IS TO BE CORRECTED AND -2 ABORTS FURTHER MASK 
	5ENTRY.')
120	MASKMT=1
	DO 125,MONTH=1,12
125	NMMASK(MASKMT,MONTH)=10
	MASKMT=2
129	MASKMT=MASKMT-1	!REENTER HERE FOR MASK REPEAT!
130	MASKMT=MASKMT+1	!REENTER HERE FOR NEXT MASK!
